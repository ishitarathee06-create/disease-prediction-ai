from reportlab.platypus import SimpleDocTemplate, Paragraph
from reportlab.lib.styles import getSampleStyleSheet
from datetime import datetime
import os


def generate_pdf(result):

    os.makedirs("reports", exist_ok=True)

    filename = f"reports/Disease_Report_{datetime.now().strftime('%Y%m%d_%H%M%S')}.pdf"

    doc = SimpleDocTemplate(filename)

    styles = getSampleStyleSheet()

    story = []

    story.append(Paragraph("<b>AI Disease Prediction Report</b>", styles["Title"]))

    story.append(Paragraph(f"<b>Date:</b> {datetime.now()}", styles["BodyText"]))

    story.append(Paragraph("<br/>", styles["BodyText"]))

    story.append(Paragraph(f"<b>Predicted Disease:</b> {result['disease']}", styles["Heading2"]))

    story.append(Paragraph(f"<b>Confidence:</b> {result['confidence']}%", styles["BodyText"]))

    story.append(Paragraph("<br/>", styles["BodyText"]))

    story.append(
        Paragraph(
            "<b>Detected Symptoms:</b><br/>" +
            "<br/>".join(result["symptoms"]),
            styles["BodyText"]
        )
    )

    story.append(Paragraph("<br/>", styles["BodyText"]))

    story.append(
        Paragraph(
            "<b>Description:</b><br/>" +
            result["description"],
            styles["BodyText"]
        )
    )

    story.append(Paragraph("<br/>", styles["BodyText"]))

    precautions = "<br/>".join(
        ["• " + p for p in result["precautions"]]
    )

    story.append(
        Paragraph(
            "<b>Precautions:</b><br/>" + precautions,
            styles["BodyText"]
        )
    )

    story.append(Paragraph("<br/>", styles["BodyText"]))

    story.append(
        Paragraph(
            "<b>Top 3 Predictions</b>",
            styles["Heading2"]
        )
    )

    for disease in result["top3"]:

        story.append(
            Paragraph(
                f"{disease['disease']} - {disease['confidence']}%",
                styles["BodyText"]
            )
        )

    story.append(Paragraph("<br/>", styles["BodyText"]))

    story.append(
        Paragraph(
            "Disclaimer: This report is generated by an AI model for educational purposes only. It is not a medical diagnosis.",
            styles["Italic"]
        )
    )

    doc.build(story)

    return filename
